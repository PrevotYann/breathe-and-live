function mergePrerequisites(base, patch = {}) {
  return { ...base, ...patch };
}

function makeSystem(entry) {
  const defaults = TYPE_DEFAULTS[entry.type] || {};
  const system = {
    ...COMMON,
    ...defaults,
    ...entry,
    tags: entry.tags || [],
    prerequisites: mergePrerequisites(COMMON.prerequisites, entry.prerequisites),
    supplement1934: { ...COMMON.supplement1934, ...entry.supplement1934 },
  };
  delete system.id;
  delete system.name;
  delete system.type;
  delete system.img;
  return system;
}

function makeDoc(entry, sort) {
  return {
    _id: entry.id,
    name: entry.name,
    type: entry.type,
    img: entry.img,
    system: makeSystem(entry),
    flags: {},
    ownership: { default: 0 },
    _stats: {
      duplicateSource: null,
      coreVersion: CORE_VERSION,
      systemId: SYSTEM_ID,
      systemVersion: SYSTEM_VERSION,
      createdTime: null,
      modifiedTime: null,
      lastModifiedBy: null,
    },
    folder: null,
    sort,
  };
}

function assertUniqueIds(items) {
  const seen = new Set();
  for (const item of items) {
    if (!item.id || seen.has(item.id)) throw new Error(`Duplicate or missing id: ${item.id}`);
    seen.add(item.id);
  }
}

